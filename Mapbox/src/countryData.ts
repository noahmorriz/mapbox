// Export default country (France) for backward compatibility
export const defaultCountry = countries.FRA;

// Helper function to get a country by any identifier (alpha2, alpha3, or name)
export const getCountry = (identifier: string): CountryData | undefined => {
  // Check if it's a direct key in our countries object (alpha3)
  if (countries[identifier]) {
    return countries[identifier];
  }
  
  // Check if it matches alpha2 code
  const byAlpha2 = Object.values(countries).find(country => 
    country.alpha2.toLowerCase() === identifier.toLowerCase()
  );
  if (byAlpha2) return byAlpha2;
  
  // Check if it matches name (case insensitive partial match)
  const byName = Object.values(countries).find(country => 
    country.name.toLowerCase().includes(identifier.toLowerCase())
  );
  if (byName) return byName;
  
  // No match found
  return undefined;
};